		<h3>Πρελούδιο</h3>
		Το φετινό καλοκαίρι μου ήταν αρκετά κουραστικό. 
		Διακοπές δεν πήγα, είχα διάβασμα και έκανα πρακτική σε ένα εργαστήριο κάπου στην Ν. Πέραμο 
		(ένα χωριό κοντά στην Καβάλα). 
		Πέρασα, λοιπόν, 2 και κάτι μήνες μακριά από τους φίλους μου και με αρκετή κούραση σε καθημερινή βάση. 
		Όμως, δεν είναι αυτός ο λόγος που ποτέ δεν θα ξεχάσω αυτό το καλοκαίρι. Ήταν το καλοκαίρι που οι θρυλικοί 
		Depeche Mode ήρθαν για συναυλία στην Ελλάδα και δεν επρόκειτο να τη χάσω με <b>ΤΙΠΟΤΑ</b>!!Δε μπορώ 
		να ξεχάσω τη στιγμή που έπιασα το "μαγικό" χαρτάκι στα χέρια μου... 
		Εννοείται ότι το εισητήριο το φυλάω σαν κόρη οφθαλμού!<br />
		Οι <b>Depeche Mode</b> ξεκίνησαν το 1976 στο Basildon της Αγγλίας με μεγάλες προοπτικές. Τη χρονική εκείνη περίοδο έκαναν δειλά δειλά τα πρώτα τους βήματα πολλά νέα συκροτήματα στην pop μουσική. Το μυστικό των <b>Depeche Mode</b> ήταν ο ήχος τους! Κάτι ανάμεσα σε electro-rock και synth-pop. Θα πρέπει να σας θυμήσω ότι στη δεκαετία του 80 έγινε η μεγαλύτερη μουσική επανάσταση! Ονόματα όπως η Madonna, ο David Bowie, οι Daft Punk και οι New Order εμαφανίστηκαν κάνοντας πάταγο, δίνοντας νόημα στην pop μουσική!
		<br /><br />
