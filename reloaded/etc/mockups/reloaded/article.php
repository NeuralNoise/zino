		<h3>Live in Athens</h3>
		Θα μπω στο θέμα όμως για να μην κουράζω! Λόγω της περσινής κυκλοφορίας του νέου του album Playing the Angel, οι Depeche Mode αποφάσισαν να κάνουν μια παγκόσμια promo-tour. Αρχικά, ξεκίνησαν από τη Νέα Υόρκη στις 28 Οκτωβρίου του 2005 και αφού έδωσαν περίπου 30 συναυλίες στις ΗΠΑ, ταξίδευσαν προς την Ευρώπη. Η τελευταία τους παράσταση ήταν -προγραμματισμένη- για το Τελ Αβίβ. Τελικά όμως, το show αυτό ακυρώθηκε και η τελευταία συναυλία δόθηκε στην Αθήνα, την 1η Αυγούστου στο TerraVibe.
		Φτάσαμε στο TerraVibe στις 19:30 περίπου, όλο άγχος για το τί επρόκειτο να ακολυθήσει. Οι πόρτες της συναυλίας άνογαν στις 17:30. Πρώτοι στη σκηνή εμφανίστηκαν οι Schiller γύρω στις 19:00. Πρόλαβα να τους ακούσω για λίγο, αλλά έστω και τα 20 λεπτά που τους είδα μου έφτασαν! Ο κόσμος ζεστάθηκε πολύ με τους ρυθμούς τους και την απίστευτη ενέργεια του μπασίστα! Έτρεχε πάνω κάτω στο stage με την κιθάρα του τροφοδοτώντας τον -ήδη- αρκετό κόσμο με τρελή ενέργεια!
		<br /><br />
		Στις 20:00 (και μέχρι τις 21:00) βγήκαν οι Raveonettes. Για να είμαι ειλικρινής εγώ αυτούς τους τύπους δεν τους ήξερα! Τελικά, δεν κέρδισα και τίποτα που τους έμαθα. Πολύ ψυχροί, στον "κόσμο" τους, χωρίς την ενέργεια και το tempo που θα περίμενε κάποιος από ένα garage-rock συγκρότημα. Ο κόσμος ξενέρωσε από το πρώτο τραγούδι και απλά υπόμενε το βασανιστήριο των τραγουδιών τους. Το πιο αξιωσημείωτο στο performance τους ήταν η -ΘΕΑ- τραγουδίστρια! Απλά δεν ξεκολλούσες τα μάτια σου από πάνω της! Όπως έμαθα αργότερα από την κολλητή μου το φόρεμά της ήταν του οίκου μόδας Dis-squared. Μη μου πείτε πως δεν τους ξέρετε;; Μέχρι κι εγώ τους ξέρω! Τέλος πάντων... Θεωρήστε το σαν την άχρηστη πληροφορία της ημέρας!
		<br /><br />
		<img src="images/depeche_mode_3.jpg" class="right" />
		Γύρω στις 21:30, λοιπόν, μετά από 3 ώρες αγωνίας έφτασε η στιγμή ο Dave και η παρέα του να βγουν στο stage να τα δώσουν όλα! Ένα πάρα πολύ θετικό στη βραδιά ήταν ο dj (αγνώστων λοιπών στοιχείων) που έπαιξε μουσική για περίπου μισή ώρα μέχρι να ετοιμαστούν και οι τελευταίες σκηνοθετικές και ηχητικές λεπτομέρειες. Τεράστια εντύπωση μου έκανε το εύρος των ηλικιών στη συναυλία. Παρατηρώντας τον κόσμο κάποιος καταλάβαινε την ασύληπτη απήχηση των Depeche Mode σε όλες τις ηλικίες! Είδα ανθρώπους 30+ , νεόυς με σχεδόν punk εμφάνιση, metal-άδες και άλλους γύρω στα 20 και κάτι. Κανείς μας δεν ένιωθε άβολα με την πολυ-συλλεκτικότητα αυτή. Είμασταν όλοι μια τεράστια φωνή που κραύγαζε: "We want more, we want more..."
		<br /><br />
		Ξαφνικά, εκεί που όλοι περιμέναμε την έναρξη, ακούγεται η εισαγωγή του "A pain I'm Used To". Τότε πραγματικά φωτίστηκε όλος ο ουρανός από τη λάμψη του stage και ξεκίνησε ο κόσμος να χειροκροτά ασταμάτητα, αποθεώνοντας τον Dave την παρέα του! Δε μπορώ να περιγράψω την ανατριχίλα που με διαπερασε όταν άκουσα την φωνή του Dave Ghahan... Πραγματικά, η σκηνή μου φάνηκε πάρα πολύ λίγη για αυτόν τον άνθρωπο! Έτρεχε πάνω κάτω ασταμάτητα, ίδρωνε, πετούσε πουκάμισα και σακάκια... Έγινε "ένα" με τον κόσμο φωνάζοντας και στριγγλίζοντας ώρες-ώρες! Στο "A question of time" ξαφνικά άρχισε να περιστρέφεται με το μικρόφωνο αγκαλιά! Σε εκείνο το σημείο σκέφτηκα: "Καλά... Τί παίρνει αυτός ο άνθρωπος;;" Εδώ πρέπει να πω ότι η σκηνοθεσία και η παραγωγή ήταν ΦΑΝΤΑΣΤΙΚΗ!! Τεράστιες οθόνες που έδειχναν καθ' όλη τη διάρκεια της συναυλίας τον κόσμο και το stage εναλλάξ από πολλές διαφορετικές κάμερες.
		<br /><br />
		Ο κόσμος (περίπου 30.000) σχεδόν κάλυπτε τη φωνή του Dave Ghahan, προσκυνώντας στον ήχο της κιθάρας του Martin Gore. Τη στιγμή που έπαιξαν το "Enjoy the Silence" ένιωσα τρομερή συγκίνηση. Ένα από τα πιο αγαπημενα μου τραγούδια, live από τη φωνή του Dave...Πραγματική ευτυχία! Μέχρι τις 23:15 περίπου που τελείωσε η συναυλία, ζούσα σε παράλληλη διάσταση. Ειλικρινά, καταλαβαίνω ότι τα συναισθήματα αυτά δε μπορώ να τα καταγράψω και μόνο όσοι ήταν εκείνο το βράδυ στη Μαλακάσσα με καταλαβαίνουν.
		<br /><br />
		<img src="images/depeche_mode_4.jpg" class="left" />
		Το μόνο που έχω να πω κλείνοντας είναι ότι δεν υπάρχει άνθρωπος που να μην ερωτευτεί τον Dave Ghahan αν τον δει live στη σκηνή! Είναι τόση η ενέργεια που δίνει στον κόσμο, η οποία σε συνδυασμό με την ένταση που υπάρχει σε κάνει να πιστεύεις ότι αυτός ο άνθρωπος "κάνει έρωτα" με το κοινό! Ξέρω ότι ακούγεται τρελό, μα αυτός ο άνθρωπος είναι ένας από τους λίγους αληθινούς performer, καταφέρνοντας να ξεπεράσει τα ναρκωτικά, τις "σκιές" που νόμιζε ότι υπήρχαν παντού και να βγει ζωντανός μετά από 2 απόπειρες αυτοκτονίας.
