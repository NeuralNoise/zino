<script type="text/javascript">
    function SwitchSettings() {
        var hash = window.location.hash( 1 );
        var validtabs = [ 'personal', 'characteristics', 'interests', 'contact', 'settings' ];
        var found = false;
        
        for ( i = 0; i < validtabs.length; ++i ) {
            if ( hash == validtabs[ i ] ) {
                document.getElementById( hash ).style.display = '';
                found = true;
            }
            else {
                document.getElementById( hash ).style.display = 'none';
            }
        }
        
        if ( !found ) {
            document.getElementById( validtabs[ 0 ] ).style.display = '';
        }
    }
    setInterval( SwitchSettings, 500 );
</script>
